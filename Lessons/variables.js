/* --------------------------------------------------------------------------
 * APUNTES:
 * 		   Tener en cuenta que las variables definidas con la palabra
 *         reservada 'var', JavaScript las declarada al principio (Hoisting)
 *         con un valor 'undefined'. Mientras que las definidas con 'let' y
 *         'const' son declaradas en las lineás donde fueron definidas,
 *         siendo 'const' inmutable.
 *
 *
 * IMPORTANTE:
 *  			  - XXX.
-------------------------------------------------------------------------- */

var myVar = '¡Hola Mundo!';

let myLet = '¡Hola Mundo!';

const myConst = '¡Hola Mundo!';

console.log(myVar, ' - ', myLet, ' - ', myConst);
